# Câu 4: Python hỗ trợ những kiểu dữ liệu cơ bản nào?
# - int (số nguyên)
# - float (số thực)
# - complex (số phức)
# - bool (đúng/sai)
# - str (chuỗi ký tự)
# - list (danh sách)
# - tuple (bộ giá trị)
# - set (tập hợp)
# - dict (từ điển - cặp key:value)

a = 10        # int
b = 3.14      # float
c = 2 + 3j    # complex
d = True      # bool
e = "Hello"   # str
f = [1,2,3]   # list
g = (1,2,3)   # tuple
h = {1,2,3}   # set
i = {"a":1, "b":2}  # dict